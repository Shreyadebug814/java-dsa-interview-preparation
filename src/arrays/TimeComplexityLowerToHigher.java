package arrays;

public class TimeComplexityLowerToHigher {
//    Complexity     | Quality    | Example                |
//            | -------------- | ---------- | ---------------------- |
//            | **O(1)**       | ⭐⭐⭐⭐⭐ Best | Array index            |
//            | **O(log n)**   | ⭐⭐⭐⭐       | Binary Search          |
//            | **O(n)**       | ⭐⭐⭐        | Single loop            |
//            | **O(n log n)** | ⭐⭐         | Merge Sort, Quick Sort |
//            | **O(n²)**      | ⭐          | Nested loops           |
//            | **O(n³)**      | Poor       | Three nested loops     |
//            | **O(2ⁿ)**      | Very Poor  | Subset recursion       |
//            | **O(n!)**      | Worst      | Permutations           |





//     Code Pattern           | Complexity     |
//            | ---------------------- | -------------- |
//            | Direct access          | **O(1)**       |
//            | Divide by 2 repeatedly | **O(log n)**   |
//            | One loop               | **O(n)**       |
//            | Efficient sorting      | **O(n log n)** |
//            | Two nested loops       | **O(n²)**      |
//            | Three nested loops     | **O(n³)**      |
//            | Every subset           | **O(2ⁿ)**      |
//            | Every permutation      | **O(n!)**      |

}
